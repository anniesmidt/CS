<h2>Backpack</h2>
<p>A backpack big enough to carry a swim suit, change of clothes, swim towel and goggles is a basic necessity of every summer day camp experience.  We like the backpacks offered at <a href="http://www.llbean.com/llb/shop/818?page=kids-backpacks" target="_blank">LL Bean</a>, for their sturdiness and size.  Younger kids may find the junior backpacks (which will just fit a smaller towel and change of clothes) to be easier to carry.
</p>

