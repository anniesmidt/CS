<h2>Clothes (what to wear)</h2>
<p>Comfortable clothes are key to a great camp experience. Think about shorts and t-shirts that are fun and lightweight. Younger kids, who may have trouble quickly changing for swimming, should have clothes that are easy to take on and off (no tricky buttons or zippers). For those extra hot days, a simple sundress is great for girls.
</p>

